@Injectable({
  providedIn: 'root'
})
export class GlobalTranslationService {
  private currentLanguage = 'en';
  private translations: any = TRANSLATIONS;
  private languageChange$ = new BehaviorSubject<string>('en');
  private translationsLoaded$ = new BehaviorSubject<boolean>(true);

  constructor(private http: HttpClient) {
    this.loadSavedLanguage();
  }

  private loadSavedLanguage(): void {
    try {
      if (typeof window !== 'undefined' && window.localStorage) {
        const savedLanguage = localStorage.getItem('esg-app-language');
        if (savedLanguage && (savedLanguage === 'en' || savedLanguage === 'ar')) {
          this.currentLanguage = savedLanguage;
          this.languageChange$.next(this.currentLanguage);
        }
      }
    } catch (error) {
      console.warn('Failed to load saved language:', error);
    }
  }

  private saveLanguage(): void {
    try {
      if (typeof window !== 'undefined' && window.localStorage) {
        localStorage.setItem('esg-app-language', this.currentLanguage);
      }
    } catch (error) {
      console.warn('Failed to save language:', error);
    }
  }

  getCurrentLanguage(): string {
    return this.currentLanguage;
  }

  getLanguageChange$(): Observable<string> {
    return this.languageChange$.asObservable();
  }

  getTranslationsLoaded$(): Observable<boolean> {
    return this.translationsLoaded$.asObservable();
  }

  toggleLanguage(): void {
    this.currentLanguage = this.currentLanguage === 'en' ? 'ar' : 'en';
    this.saveLanguage();
    this.languageChange$.next(this.currentLanguage);
  }

  setLanguage(language: 'en' | 'ar'): void {
    if (language !== this.currentLanguage) {
      this.currentLanguage = language;
      this.saveLanguage();
      this.languageChange$.next(this.currentLanguage);
    }
  }

  translate(key: string): string {
    try {
      const keys = key.split('.');
      let value = this.translations[this.currentLanguage];
      
      for (const k of keys) {
        value = value?.[k];
      }
      
      if (!value) {
        console.warn(`❌ Translation not found for key: ${key} in language: ${this.currentLanguage}`);
        return key;
      }
      
      return value;
    } catch (error) {
      console.error(`❌ Translation error for key: ${key}`, error);
      return key;
    }
  }

  isRTL(): boolean {
    return this.currentLanguage === 'ar';
  }

  isTranslationsLoaded(): boolean {
    return this.translationsLoaded$.value;
  }

  getTranslations(): any {
    return this.translations;
  }

  getLanguageFlag(): string {
    return this.currentLanguage === 'en' ? '🇺🇸' : '🇸🇦';
  }

  getLanguageText(): string {
    return this.currentLanguage === 'en' ? 'English' : 'العربية';
  }

  getButtonTitle(): string {
    return this.currentLanguage === 'en'
      ? 'Switch to Arabic'
      : 'التبديل إلى الإنجليزية';
  }
} 